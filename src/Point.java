//Point class which contains x and y coordinates of a location specified in the input text file. 
public class Point {
	private int x;
	private int y;
	
	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public Point (int xCoor, int yCoor) {
		this.x = xCoor;
		this.y = yCoor;
	}
	
	
}
